class CrewUser {
  final String crewID;
  final String authID;
  final String name;
  final String bio;
  final String crewName;
  final int coffeeIntensity;
  final int sugarIntensity;
  final bool isAdmin;

  CrewUser({
    this.crewID,
    this.authID,
    this.name,
    this.bio,
    this.crewName,
    this.coffeeIntensity,
    this.sugarIntensity,
    this.isAdmin,
  });
}
